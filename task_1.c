// Write a C program to perform input/output of all basic data types.
#include <stdio.h>

int main() {

    int num;
    printf("Enter your number: ");
    scanf("%d", &num);
    printf("Your number is: %d\n", num);
    
    char ch;
    printf("Enter your char: ");
    scanf("%s", &ch);
    printf("Your char is: %c\n", ch);
    
    float fNum;
    printf("Enter your float number: ");
    scanf("%s", &fNum);
    printf("Your float number is: %f\n", fNum);
    

    return 0;
}